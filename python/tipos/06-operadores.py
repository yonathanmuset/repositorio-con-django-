# operadores
print(3 + 4)
print(3 - 4)
print(3 * 4)
print(3 / 4)
print(3 // 4)
print(3**4)
print(50 % 4)

# operadores comparattivos
print(3 < 4)
print(3 > 4)
print(3 <= 4)
print(3 >= 4)
print(3 == 4)


# operadores logicos
print(3 < 4 and "hola" < "pyhton")
print(3 < 4 or "hola" < "pyhton")
print(not (3 > 4))
